/**
 * Widen string-literal leaves to `string` while preserving the nested
 * shape and keeping PluralLeaf intact. This lets `ru.ts` declare its own
 * string content while TS still enforces that every key in `en` exists.
 */
type WidenLeaves<T> = T extends PluralLeaf
  ? PluralLeaf
  : T extends string
    ? string
    : T extends object
      ? { [K in keyof T]: WidenLeaves<T[K]> }
      : T;

export type MessageDict = WidenLeaves<typeof en>;
